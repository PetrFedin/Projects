const PAIRS = [...PAIRS_RAW].sort((a, b) => b[0].length - a[0].length);

/** Collapse invalid Tailwind opacity stacks like `bg-accent-primary/10/60` → `bg-accent-primary/10`. */
function stripDoubleTokenOpacity(s) {
  return s.replace(
    /\b((?:[\w[\]%-]+:)*)([a-z-]+)-(accent-primary|text-primary)\/(\d+)\/(\d+)/g,
    (_, pfx, pref, tok, a) => `${pfx}${pref}-${tok}/${a}`
  );
}

function migrate(content) {
  let s = content;
  for (const [a, b] of PAIRS) {
    s = s.split(a).join(b);
  }
  s = stripDoubleTokenOpacity(s);
  return s;
}

function walk(dir, out = []) {
  for (const name of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, name.name);
    if (name.isDirectory()) {
      if (name.name === 'node_modules' || name.name === '.next') continue;
      walk(p, out);
    } else if (/\.(tsx|ts|mdx)$/.test(name.name)) {
      out.push(p);
    }
  }
  return out;
}

const dry = process.env.DRY_RUN === '1';
let filesChanged = 0;

for (const file of walk(ROOT)) {
  const before = fs.readFileSync(file, 'utf8');
  if (!/\b(slate|indigo|zinc|violet|purple|fuchsia|pink)-/.test(before)) continue;
  const after = migrate(before);
  if (after === before) continue;
  filesChanged += 1;
  if (!dry) fs.writeFileSync(file, after, 'utf8');
}

