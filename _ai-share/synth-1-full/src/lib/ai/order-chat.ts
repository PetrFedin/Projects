/**
 * OroCommerce: AI-чат по заказам — бот по наличию, ценам, статусу.
 * Пользователь спрашивает текстом → бот отвечает на основе данных заказов/каталога.
 */

export interface OrderChatMessage {
  id: string;
  role: 'user' | 'assistant';
  content: string;
  timestamp: string;
}

/** Демо: простые ответы по ключевым словам. В проде — LLM + RAG по заказам и каталогу. */
export function getOrderChatReply(userMessage: string): string {
  const q = userMessage.toLowerCase().trim();
  if (/\b(наличи|остатк|есть ли|есть в наличии)\b/.test(q)) {
    return 'По текущему каталогу FW26 основные позиции в наличии. Остатки по артикулам можно уточнить в карточке товара или у менеджера.';
  }
  if (/\b(цен|прайс|стоимость|сколько стоит)\b/.test(q)) {
    return 'Цены указаны в лайншите и в матрице заказа. Оптовые цены зависят от объёма и договора. Для персонального КП создайте запрос котировок (RFQ).';
  }
  if (/\b(статус|где заказ|отследит|доставк)\b/.test(q)) {
    return 'Статус заказа можно посмотреть в разделе «Мои заказы». После отгрузки придёт трек-номер. Нужен номер заказа?';
  }
  if (/\b(срок|когда|дедлайн)\b/.test(q)) {
    return 'Сроки поставки по коллекции FW26 — до 4 недель с момента подтверждения заказа. Точные даты в карточке заказа.';
  }
  if (/\b(moq|минимум|минимальный заказ)\b/.test(q)) {
    return 'MOQ зависит от позиции: обычно от 6 шт. по артикулу или от 24 шт. по стилю. Уточняется в матрице заказа.';
  }
  return 'Могу подсказать по наличию, ценам, статусу заказа и срокам. Напишите, что интересует.';
}
