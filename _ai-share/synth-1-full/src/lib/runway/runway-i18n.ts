export type RunwayI18nKey = keyof (typeof RUNWAY_STRINGS)['ru'];

let activeLocale: RunwayLocale = 'ru';

export function setRunwayLocale(locale: RunwayLocale): void {
  activeLocale = locale;
}

export function getRunwayLocale(): RunwayLocale {
  return activeLocale;
}

/** t('runway.addToCart') — подстановка {placeholders}. */
export function t(key: RunwayI18nKey, params?: Record<string, string | number>): string {
  const table = RUNWAY_STRINGS[activeLocale] ?? RUNWAY_STRINGS.ru;
  let text: string = table[key] ?? RUNWAY_STRINGS.ru[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return text;
}

/** Все ключи для тестов и документации. */
export function listRunwayI18nKeys(): RunwayI18nKey[] {
  return Object.keys(RUNWAY_STRINGS.ru) as RunwayI18nKey[];
}

/** Проверка паритета RU/EN — для CI-тестов i18n. */
export function verifyRunwayI18nParity(): { ok: boolean; missingInEn: RunwayI18nKey[] } {
  const ruKeys = listRunwayI18nKeys();
  const enKeys = new Set(Object.keys(RUNWAY_STRINGS.en));
  const missingInEn = ruKeys.filter((k) => !enKeys.has(k));
  return { ok: missingInEn.length === 0, missingInEn };
}

type HeroNarrativeField = 'title' | 'description';

/** RU из JSON (sectionTitle/sectionDescription), EN — из i18n hero.* ключей. */
export function resolveSectionNarrative(
  productSlug: string,
  sectionIndex: number,
  field: HeroNarrativeField,
  fallbackRu?: string
): string {
  const heroKey = `runway.hero.${productSlug}.${sectionIndex}.${field}` as RunwayI18nKey;
  if (activeLocale === 'en') {
    const en = RUNWAY_STRINGS.en[heroKey];
    if (en) return en;
  }
  if (activeLocale === 'ru') {
    const ru = RUNWAY_STRINGS.ru[heroKey];
    if (ru) return ru;
  }
  return fallbackRu?.trim() ?? '';
}
