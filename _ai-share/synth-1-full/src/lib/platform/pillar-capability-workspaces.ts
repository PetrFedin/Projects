export function getPillarCapabilityWorkspace(
  workspaceId: string
): PillarCapabilityWorkspace | undefined {
  return PILLAR_CAPABILITY_WORKSPACES[workspaceId];
}

export function resolvePillarWorkspaceFeatureId(
  workspace: PillarCapabilityWorkspace,
  requestedId: string | null | undefined
): string {
  if (!requestedId) return workspace.defaultFeatureId;
  const found = workspace.features.find((f) => f.id === requestedId);
  if (!found || found.status === 'planned') return workspace.defaultFeatureId;
  return found.id;
}

export function getPillarWorkspaceFeature(
  workspace: PillarCapabilityWorkspace,
  featureId: string
): PillarCapabilityFeature {
  return (
    workspace.features.find((f) => f.id === featureId) ??
    workspace.features.find((f) => f.id === workspace.defaultFeatureId)!
  );
}
