/**
 * Подсказки по габаритам для досье (таблица базового размера), только для L1 «Сумки».
 */
export function getSuggestedBagDimensionsForBagTypeParameterId(
  bagTypeParameterId: string | undefined
): Record<string, string> | undefined {
  if (!bagTypeParameterId) return undefined;
  const row = BAG_DIMS_BY_PARAMETER_ID[bagTypeParameterId];
  if (!row || Object.keys(row).length === 0) return undefined;
  return { ...row };
}
