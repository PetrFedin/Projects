/** Поиск предложения по id (для строк матрицы «Темы покрытия»). */
export function getNamingProposalById(id: string) {
  return CABINET_SIDEBAR_NAMING_PROPOSALS.find((p) => p.id === id);
}

/** Краткий список для вставки в UI или PR */
export function cabinetNamingProposalsSummary(): string {
  return CABINET_SIDEBAR_NAMING_PROPOSALS.map(
    (p) => `• ${p.id}: ${p.proposal.replace(/\*\*/g, '')}`
  ).join('\n');
}
