/**
 * Параметры производства по типам категорий.
 * Габариты, размерные шкалы, размеры, примерки, семплы, доставки, оплаты, налоги, материалы, фурнитура
 * — всё привязано к аудитории и категориям L1/L2/L3 из справочников (admin).
 */

export type ProductionParamKey =
  | 'dimensions' // габариты
  | 'sizeScale' // размерные шкалы EU/RU/INT
  | 'sizes' // размеры производства
  | 'fittings' // примерки
  | 'samples' // семплы
  | 'deliveries' // доставки
  | 'payments' // оплаты
  | 'taxes' // налоги
  | 'materials' // материалы
  | 'trims'; // фурнитура

export interface CategoryProductionParams {
  /** Категория L1 id (apparel, shoes, accessories, bags, headwear, fur) */
  catL1Id: string;
  label: string;
  /** Размерные шкалы: EU, RU, INT, обувь EU (36-46), детская и т.д. */
  sizeScales: { id: string; label: string; sizes: string[]; rule: string }[];
  /** Габариты: какие мерки используем (обхват груди, длина, обхват талии и т.д.) */
  dimensions: string[];
  /** Материалы: типы (ткань, кожа, трикотаж, мех) */
  materialTypes: string[];
  /** Фурнитура: молнии, пуговицы, липучки, подкладка и т.д. */
  trims: string[];
  /** Этапы примерок (Proto1, TOP, PP и т.д.) */
  fittingStages: string[];
  /** Особенности доставки */
  deliveryNotes?: string;
  /** Особенности налогов (мех — спец. пошлины) */
  taxNotes?: string;
}

